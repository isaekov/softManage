package ru.hwru.softmanage.enums;

public enum ProjectStatus {

    // Проект создан, но ещё не утверждён. Идёт согласование КП с клиентом
    DRAFT("Черновик"),

    // КП утверждено, идёт декомпозиция на задачи, оценка сроков и ресурсов.
    PLANNING("Планирование"),

    // Команда выполняет задачи. Проект в активной фазе разработки.
    ACTIVE("В работе"),

    // Работы приостановлены (по инициативе клиента или внутренним причинам).
    ON_HOLD("На паузе"),

    // Все задачи выполнены, продукт сдан клиенту, акты подписаны.
    COMPLETED("Завершён"),

    // Проект закрыт досрочно (клиент отказался, нет бюджета и т.д.).
    CANCELLED("Отменён");

    private final String label;

    ProjectStatus(String label) {
        this.label = label;
    }

    public String getLabel() {
        return label;
    }

}
